  private$estivationDay        = estivationDay
  private$energyPreG           = energyPreG
  private$preGsugar            = preGsugar
  private$energyFromBlood_b    = energyFromBlood_b
  private$S_u                  = S_u
  private$omega                = omega
  private$S_sb                 = S_sb
  private$S_sa                 = S_sa
  private$S_w                  = S_w
  private$S_p                  = S_p
  private$Bs_surv              = Bs_surv
